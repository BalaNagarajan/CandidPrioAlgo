package com.forum.feedback.priority.model;

public class Reviewer {

	private String reviewerName;
	private int reviewerPriority;

	public String getReviewerName() {
		return reviewerName;
	}

	public void setReviewerName(String reviewerName) {
		this.reviewerName = reviewerName;
	}

	public int getReviewerPriority() {
		return reviewerPriority;
	}

	public void setReviewerPriority(int reviewerPriority) {
		this.reviewerPriority = reviewerPriority;
	}

}
